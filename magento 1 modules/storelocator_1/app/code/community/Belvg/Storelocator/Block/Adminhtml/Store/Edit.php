class Belvg_Storelocator_Block_Adminhtml_Store_Edit extends Mage_Adminhtml_Block_Widget_Form_Container
{
    /**
     * Initialize edit form container
     *
     */
    public function __construct()
    {
        $this->_objectId = 'id';
        $this->_blockGroup = 'storelocator';
        $this->_controller = 'adminhtml_store';

        parent::__construct();

        $this->_updateButton('save', 'label', Mage::helper('storelocator')->__('Save Item'));
        $this->_addButton('saveandcontinue', array(
            'label' => Mage::helper('adminhtml')->__('Save and Continue Edit'),
            'onclick' => 'saveAndContinueEdit()',
            'class' => 'save',
        ), -100);

        $this->_updateButton('delete', 'label', Mage::helper('storelocator')->__('Delete Item'));

        $this->_formScripts[] = "
            function saveAndContinueEdit(){
                editForm.submit($('edit_form').action+'back/edit/');
            }
        ";
    }

    /**
     * Retrieve text for header element depending on loaded page
     *
     * @return string
     */
    public function getHeaderText()
    {
        $model = Mage::helper('storelocator')->getItemInstance();
        if ($model->getEntityId()) {
            return Mage::helper('storelocator')->__("Edit Item '%s'", $this->escapeHtml($model->getTitle()));
        } else {
            return Mage::helper('storelocator')->__('New Item');
        }
    }
}
